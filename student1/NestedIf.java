public class NestedIf
{
    public static void main(String []args)
    {
        int num = 10;

            if (num < 0)
            {
            System.out.println("number is negative");
            }
            else if(num == 0)
            {
                System.out.println("number is zero");
            }
            else
            {
                System.out.println("number is positive");
            }


    }
}
