public class ExampleJava1
{
    public static void main(String[] args)
    {
        int[] x = { 120, 200, 016 };

        System.out.println(x[0]);
        System.out.println(x[1]);
        System.out.println(x[2]);

        for (int i = 0; i < x.length; i++)
            System.out.print(x[i] + " ");
    }
}
