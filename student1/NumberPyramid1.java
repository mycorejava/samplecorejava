public class NumberPyramid1
{
    public static void main(String[] args)
    {
        int num =5;

        for(int i=0;i<num;i++)
        {
            for(int j=i+1;j<num;j++)
            {
                System.out.print("% ");

            }

            System.out.println();
        }

    }
}
